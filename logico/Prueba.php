package Logico;

public class Prueba {

    String codigoPrueba, distanciaPrueba, sexoPrueba, categoria, tipoPrueba;

    public String getTipoPrueba() {
        return tipoPrueba;
    }

    public void setTipoPrueba(String tipoprueba) {
        this.tipoPrueba = tipoprueba;
    }

    public String getCodigoPrueba() {
        return codigoPrueba;
    }

    public void setCodigoPrueba(String codigoPrueba) {
        this.codigoPrueba = codigoPrueba;
    }

    public String getDistanciaPrueba() {
        return distanciaPrueba;
    }

    public void setDistanciaPrueba(String distanciaPrueba) {
        this.distanciaPrueba = distanciaPrueba;
    }

    public String getSexoPrueba() {
        return sexoPrueba;
    }

    public void setSexoPrueba(String sexoPrueba) {
        this.sexoPrueba = sexoPrueba;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }
}
